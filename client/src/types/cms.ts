export interface Article {
 id: number;
 documentId: string;
 title: string;
 description: string | null;
 slug: string;
 createdAt: string;
 updatedAt: string;
 publishedAt: string;
 content: Content[];
 cover?: Cover | null;
 author?: Author | null;
 category: Category | null;
 blocks: [];
}

export interface Content {
 type: string;
 level?: number;
 children: {
  text: string;
  type: string;
 }[];
}

export interface Cover {
 id: number;
 documentId: string;
 name: string;
 alternativeText: string | null;
 caption: string | null;
 width?: number | null;
 height?: number | null;
 url: string;
 formats?: Record<
  string,
  {
   url: string;
   width?: number;
   height?: number;
  }
 >;
}

export interface Author {
 id: number;
 documentId: string;
 name: string;
 email: string;
 createdAt: string;
 updatedAt: string;
 publishedAt: string;
}

export interface Category {
 id: number;
 documentId: string;
 name: string;
 slug?: string;
}

export type StrapiID = number;

export interface StrapiListResponse<T> {
 data: T[];
 meta: {
  pagination?: {
   page: number;
   pageSize: number;
   pageCount: number;
   total: number;
  };
 };
}

export interface StrapiSingleResponse<T> {
 data: T | null;
 meta?: Record<string, unknown>;
}

// Legacy nested shapes (kept for reference if needed)
export interface UploadFileAttributes {
 name: string;
 alternativeText: string | null;
 caption: string | null;
 width?: number | null;
 height?: number | null;
 url: string;
 formats?: Record<
  string,
  {
   url: string;
   width?: number;
   height?: number;
  }
 >;
}

export interface MediaRelation {
 data: {
  id: StrapiID;
  attributes: UploadFileAttributes;
 } | null;
}

export interface AuthorRef {
 data: {
  id: StrapiID;
  attributes: {
   name: string;
  };
 } | null;
}

export interface CategoryRef {
 data: {
  id: StrapiID;
  attributes: {
   name: string;
   slug?: string;
  };
 } | null;
}

export interface ArticleAttributes {
 title: string;
 description?: string | null;
 slug: string;
 cover?: MediaRelation;
 author?: AuthorRef;
 category?: CategoryRef;
 publishedAt?: string;
 createdAt?: string;
 updatedAt?: string;
}
